package helper;

//用户信息类
public class User {
	private int UserID;
	private String UserName;
	private String UserPwd;
	private String UserStar;
	private String UserBuyCar;
	private int UserGrant;
	private String UserAddress;
	private String UserPhone;
	public String getUserName() {
		return UserName;
	}
	public void setUserName(String userName) {
		UserName = userName;
	}
	public String getUserPwd() {
		return UserPwd;
	}
	public void setUserPwd(String userPwd) {
		UserPwd = userPwd;
	}
	public String getUserStar() {
		return UserStar;
	}
	public void setUserStar(String userStar) {
		UserStar = userStar;
	}
	public String getUserBuyCar() {
		return UserBuyCar;
	}
	public void setUserBuyCar(String userBuyCar) {
		UserBuyCar = userBuyCar;
	}
	public int getUserGrant() {
		return UserGrant;
	}
	public void setUserGrant(int userGrant) {
		UserGrant = userGrant;
	}
	public String getUserAddress() {
		return UserAddress;
	}
	public void setUserAddress(String userAddress) {
		UserAddress = userAddress;
	}
	public String getUserPhone() {
		return UserPhone;
	}
	public void setUserPhone(String userPhone) {
		UserPhone = userPhone;
	}
	public int getUserID() {
		return UserID;
	}
	public void setUserID(int userID) {
		UserID = userID;
	}
}
